    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        ClienteDAO obj = new ClienteDAO();
        String [] a = new String[4];
        a[0]= txtnombrec.getText();
        a[1]=txtdireccion.getText();
        a[2]=txttelefono.getText();
        a[3]=txtcredito.getText();
        boolean bandera = Utilidades.hayVacios(a);
       boolean ban = false;
        if(bandera == true){
            Utilidades.confirma(confirma,"Por favor ingresa todos los datos");
        }else{
            try {
                Double.parseDouble(a[3]);
          ban  =   obj.almacena(a);
                Utilidades.confirma(confirma, "Datos ingresados correctamente");
            } catch (ClassNotFoundException ex) {
             Utilidades.confirma(confirma, "Ocurrio un error en el sistema");
            } catch (SQLException ex) {
                Utilidades.confirma(confirma, "Ocurrio un error con la conexion a la base de datos");
            }catch(NumberFormatException e){
                Utilidades.confirma(confirma, "Por favor revisa los datos que ingresaste");
            }
            if(ban==true){
            txtnombrec.setText("");
            txtdireccion.setText("");
            txttelefono.setText("");
            txtcredito.setText("");
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


